package com.my.megabox.screen;

public class ScreenDTO {

	private Integer s_num;
	private String s_name;
	private Integer s_seat;
	private Integer s_status;
	private Integer c_num;

	public Integer getS_num() {
		return s_num;
	}

	public void setS_num(Integer s_num) {
		this.s_num = s_num;
	}

	public String getS_name() {
		return s_name;
	}

	public void setS_name(String s_name) {
		this.s_name = s_name;
	}

	public Integer getS_seat() {
		return s_seat;
	}

	public void setS_seat(Integer s_seat) {
		this.s_seat = s_seat;
	}

	public Integer getS_status() {
		return s_status;
	}

	public void setS_status(Integer s_status) {
		this.s_status = s_status;
	}

	public Integer getC_num() {
		return c_num;
	}

	public void setC_num(Integer c_num) {
		this.c_num = c_num;
	}

}